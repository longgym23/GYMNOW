class UserEntity {
  final String id;
  final String email;
  final String name;
  final double height;
  final double weight;
  final int age;
  final String role;

  UserEntity({
    required this.id,
    required this.email,
    required this.name,
    required this.height,
    required this.weight,
    required this.age,
    required this.role,
  });
}
